/**
 * op_add-function that adds to integers.
 * @a,b the 2 parameter that will be added.
 * return int that reperesen the sum .
 */
int op_add(int a, int b)
{	
	return (a + b);
}

/**
 * op_sub-function that substranct an integer from another.
 * @a:the first parameter.
 * @b:the second parameter
 * returns the difference of a and b
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul-function that multiply 2 integers.
 * @a:the first parameter.
 * @b:the second parameter.
 * returns the product of a and b.
 */
int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div-function that divide 2 integers.
 * @a:the first parameter.
 * @b:the second parameter.
 * returns the result of the division of a by b.
 */
int op_div(int a, int b)
{       
        return (a / b);
}

/**
 * op_mod-function that get the remainder of divisionbetween 2 integers.
 * @a:the first parameter.
 * @b:the second parameter.
 * returns the remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
        return (a % b);
}
